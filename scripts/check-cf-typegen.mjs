import { spawn } from 'node:child_process';
import { mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { buildCloudflareWranglerConfig } from './create-cf-wrangler-config.mjs';
import { resolveRequiredSiteKey } from './lib/site-config.mjs';
import {
  createCanonicalTypegenContract,
  resolveSiteDeployContract,
} from './lib/site-deploy-contract.mjs';

const rootDir = process.cwd();
const trackedTypesPath = path.resolve(
  rootDir,
  'src/shared/types/cloudflare.d.ts'
);
const generatedByWranglerPattern =
  /^\/\/ Generated by Wrangler by running `wrangler types .*?` \(hash: [^)]+\)$/m;
const stableGeneratedByWranglerComment =
  '// Generated by Wrangler by running `wrangler types --config <generated-config> --env-interface CloudflareEnv <tracked-types-path>` (hash: c0915f221de7995aa979a1ffef4986c7)';

export function normalizeTrackedTypes(content) {
  return content
    .replace(generatedByWranglerPattern, stableGeneratedByWranglerComment)
    .replace(/[ \t]+$/gm, '');
}

async function createCanonicalTypegenWranglerConfig({
  rootPath = rootDir,
  siteKey = resolveRequiredSiteKey(process.env),
} = {}) {
  const baseContract = resolveSiteDeployContract({
    rootDir: rootPath,
    siteKey,
  });
  const contract = createCanonicalTypegenContract(baseContract);
  const tempDir = await mkdtemp(path.join(os.tmpdir(), 'cf-typegen-'));
  const outputPath = path.join(tempDir, 'wrangler.cloudflare.typegen.toml');
  const templatePath = path.resolve(
    rootPath,
    contract.router.wranglerConfigRelativePath
  );
  const template = await readFile(templatePath, 'utf8');
  const content = buildCloudflareWranglerConfig({
    template,
    contract,
    workerSlot: 'router',
    templatePath,
    outputPath,
    validateTemplateContract: true,
  });

  await writeFile(outputPath, content, 'utf8');

  return {
    contract,
    configPath: outputPath,
    async cleanup() {
      await rm(tempDir, { recursive: true, force: true });
    },
  };
}

function runWranglerTypes(configPath) {
  return new Promise((resolve, reject) => {
    const child = spawn(
      'pnpm',
      [
        'exec',
        'wrangler',
        'types',
        '--config',
        configPath,
        '--env-interface',
        'CloudflareEnv',
        trackedTypesPath,
      ],
      {
        cwd: rootDir,
        env: process.env,
        stdio: 'inherit',
      }
    );

    child.once('error', reject);
    child.once('exit', (code) => {
      if (code !== 0) {
        reject(new Error(`wrangler types exited with code ${code}`));
        return;
      }

      resolve(undefined);
    });
  });
}

async function main() {
  const artifacts = await createCanonicalTypegenWranglerConfig();

  try {
    const before = await readFile(trackedTypesPath, 'utf8');
    await runWranglerTypes(artifacts.configPath);
    const generated = await readFile(trackedTypesPath, 'utf8');
    await writeFile(trackedTypesPath, before, 'utf8');

    if (normalizeTrackedTypes(before) !== normalizeTrackedTypes(generated)) {
      throw new Error(
        'src/shared/types/cloudflare.d.ts is out of date. Run `pnpm cf:typegen` and commit the result.'
      );
    }
  } finally {
    await artifacts.cleanup();
  }

  console.log(
    '[cf:typegen:check] src/shared/types/cloudflare.d.ts is up to date'
  );
}

export { createCanonicalTypegenWranglerConfig };

if (
  process.argv[1] &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)
) {
  main().catch((error) => {
    console.error(
      error instanceof Error ? error.stack || error.message : String(error)
    );
    process.exit(1);
  });
}
